function Product( name, id , price, screen, backCamera, frontCamera, img, desc, type, quantity){
    this.name= name;
    this.id = id;
    this.price = price;
    this.screen = screen;
    this.backCamera = backCamera;
    this.frontCamera = frontCamera;
    this.img = img;
    this.desc = desc;
    this.type =type;
    this.quantity=quantity;
}